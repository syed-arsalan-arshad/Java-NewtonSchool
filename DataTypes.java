public class DataTypes {
    
    public static void main(String[] args) {
        //integer
        int num1 = 10;
        int num2 = 20;
        int result = num1 + num2;
        System.out.println("Sum of Two Integer: "+result);

        //float
        float num3 = 13.5f;
        float num4 = 14.5f;
        System.out.println("Sum of Two Float: "+(num3+num4));
    }
}
